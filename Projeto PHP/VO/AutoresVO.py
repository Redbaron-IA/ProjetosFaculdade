class Autores:
    
    def __init__(self, idAutor, autores):
        self.idAutor = idAutor
        self.autores = autores

    def get_idAutor(self):
        return self.idAutor

    def set_idAutor(self, idAutor):
        self.idAutor = idAutor

    def get_autores(self):
        return self.autores

    def set_autores(self, autores):
        self.autores = autores
